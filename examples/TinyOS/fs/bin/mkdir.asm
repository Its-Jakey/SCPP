; Check that there is at least 1 argument
LD @R0 @ARGS_LENGTH
LDI @R1 1
CMP @R0 @R1
JNZ @ARGS_ERROR

; Verify that the directory does not allready exist
LD @R15 @ARG_0
SYSCALL @GET_ABSOLUTE_PATH
MOV @R0 @R15
SYSCALL @FILE_EXISTS
JE @PATH_EXISTS_ERROR

; Create the directory
MOV @R15 @R0
SYSCALL @CREATE_DIRECTORY
HLT

:ARGS_ERROR
LDI @R15 'Invalid argument count'
SYSCALL @PRINT_STRING
HLT

:PATH_EXISTS_ERROR
LDI @R15 'Duplicate directory: '
LD @R0 @ARGS_START
CON @R15 @R0
SYSCALL @PRINT_STRING
HLT

:PATH_ERROR
LDI @R15 'Invalid file path: '
LD @R0 @ARGS_START
CON @R15 @R0
SYSCALL @PRINT_STRING
HLT