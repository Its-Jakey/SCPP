
:ARGS_CHECK
LD @R0 @ARGS_LENGTH
LDI @R1 1
CMP @R0 @R1
JNZ @ARGS_ERROR

:PATH_CHECK
LD @R15 @ARGS_START
SYSCALL @GET_ABSOLUTE_PATH
MOV @R0 @R15
SYSCALL @FILE_EXISTS
JNE @PATH_ERROR

:DIRECTORY_CHECK
MOV @R15 @R0
SYSCALL @FILE_IS_DIRECTORY
JNE @PATH_ERROR

:CD
MOV @R15 @R0
SYSCALL @SET_CURRENT_DIRECTORY
HLT

:ARGS_ERROR
LDI @R15 'Invalid argument count'
SYSCALL @PRINT_STRING
HLT

:PATH_ERROR
LDI @R15 'Invalid directory: '
LD @R0 @ARGS_START
CON @R15 @R0
SYSCALL @PRINT_STRING
HLT